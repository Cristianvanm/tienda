package com.bosque.DTO;

public class Provee {
	private int acednit;
	private String bciudad;
	private String cdireccion;
	private String demail;
	private String etelefono;
	public int getAcednit() {
		return acednit;
	}
	public void setAcednit(int acednit) {
		this.acednit = acednit;
	}
	public String getBciudad() {
		return bciudad;
	}
	public void setBciudad(String bciudad) {
		this.bciudad = bciudad;
	}
	public String getCdireccion() {
		return cdireccion;
	}
	public void setCdireccion(String cdireccion) {
		this.cdireccion = cdireccion;
	}
	public String getDemail() {
		return demail;
	}
	public void setDemail(String demail) {
		this.demail = demail;
	}
	public String getEtelefono() {
		return etelefono;
	}
	public void setEtelefono(String etelefono) {
		this.etelefono = etelefono;
	}
}
